void blit_cols(const char *p, char h, char r, char c, char x, char y) {
    // optimizar, cachos de columna en vez de pixel a pixel
    char i, j, t, b;
    for(t=0; t<r; t++) {
        for(i=0; i<c; i++) {
            for(j=0; j<8; j++) {
                b = pgm_read_byte(p+i+t*c);
                if (b & (1 << j)) {
                    setPixel (x+i, y+j+t*8);
                }
            }
        }
    }
};
